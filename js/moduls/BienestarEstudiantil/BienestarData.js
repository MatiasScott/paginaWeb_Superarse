    function inicializarAnimacionPasos() {
        const pasos = document.querySelectorAll('.paso');
        
        if (pasos.length === 0) {
            return;
        }

        if (window.pasosObserver) {
            window.pasosObserver.disconnect();
        }

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const pasoElement = entry.target;
                    const index = Array.from(pasos).indexOf(pasoElement);
                    setTimeout(() => {
                        pasoElement.classList.add('mostrar');
                    }, index * 200);
                } else {
                    entry.target.classList.remove('mostrar');
                }
            });
        }, {
            threshold: 0.5
        });

        pasos.forEach(paso => {
            observer.observe(paso);
        });

        window.pasosObserver = observer;
    }

